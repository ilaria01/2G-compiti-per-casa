#include <iostream>

using namespace std;

int main ()
{
signed char max=127;
signed char min=-128;
// il numero massimmo è 127
// il numero minimo è -128
cout << "Ecco il minimo" << (int)min << endl;
cout << "Ecco il massimo" << (int)max << endl;
cout << "Ecco il minimo-1" << min-1 << endl;
cout << "Ecco il massimo+1" << max+1 << endl;
return 0;
}
